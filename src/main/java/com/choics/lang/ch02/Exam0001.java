// 화면에 글자 출력하기

package com.choics.lang.ch02;

public class Exam0001 {
  public static void main(String[] args) {
    System.out.println("Hello");
    System.out.print("Hello"); // System.out.print() 출력 후, 줄바꿈 안함
    System.out.println("Hello"); 



// 사칙연산

    System.out.println("3 + 5"); // "" 는 문자열이기 때문에 3 + 5 가 그대로 출력 됨
    
    System.out.println(5 + 3); // 덧셈 값이 출력 됨
    System.out.println(5 - 3);
    System.out.println(5 * 3);
    System.out.println(5 / 3); // 1 출력 됨
    System.out.println(5 / 0.3); // 1.66666.... 출력 됨
  }
  
}

