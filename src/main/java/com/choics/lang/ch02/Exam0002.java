package com.choics.lang.ch02;

public class Exam0002 {
  public static void main(String[] args) {
    
    int x = 4 , y = 2;          // ' = ' 는 대입연산자
    
    System.out.println(x + y);
    System.out.println(x - y);
    System.out.println(x * y);
    System.out.println(x / y);

  }
  
}
